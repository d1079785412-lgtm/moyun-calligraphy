export const scripts = ["楷书", "隶书"];

export const scriptMasterMap = {
  楷书: "褚遂良",
  隶书: "曹全碑",
};

export const masters = Object.values(scriptMasterMap);

export const formats = ["中堂", "对联", "条幅"];

export const formatMaxChars = {
  中堂: 50,
  条幅: 50,
  对联: 50,
};

export const defaultCharsPerLine = {
  中堂: 8,
  条幅: 10,
  对联: 7,
};

export function getFormatMaxChars(format) {
  return formatMaxChars[format] || 50;
}

export function normalizeCharsPerLine(value, format) {
  const max = getFormatMaxChars(format);
  const parsed = Number.parseInt(value, 10);
  if (!Number.isFinite(parsed)) return defaultCharsPerLine[format] || Math.min(max, 8);
  return Math.max(1, Math.min(parsed, max));
}

function splitCoupletItems(items) {
  if (items.length <= 1) return [items];
  const firstLineLength = Math.max(1, Math.floor(items.length / 2));
  return [items.slice(0, firstLineLength), items.slice(firstLineLength)];
}

