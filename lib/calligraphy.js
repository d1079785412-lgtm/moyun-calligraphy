export function normalizeScriptAndMaster({ script, master } = {}) {
  const normalizedScript = scripts.includes(script) ? script : "楷书";
  return {
    script: normalizedScript,
    master: scriptMasterMap[normalizedScript] || master || "褚遂良",
  };
}

