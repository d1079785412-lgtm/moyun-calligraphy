function escapeXml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function splitChars(text) {
  return Array.from(text || "墨韵智创").slice(0, 50);
}

function chunkChars(chars, size) {
  const chunkSize = Math.max(1, size || chars.length || 1);
  const chunks = [];
  for (let index = 0; index < chars.length; index += chunkSize) {
    chunks.push(chars.slice(index, index + chunkSize));
  }
  return chunks;
}

